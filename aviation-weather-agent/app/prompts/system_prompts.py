# ==================== 安全约束通用模板 ====================

SAFETY_CONSTRAINTS_TEMPLATE = """
【安全约束（通用）】
1. **数据真实性**：
   - 禁止编造或推测气象数据
   - 仅基于提供的METAR报文进行分析
   - 缺失数据明确标注"未知"或"数据缺失"

2. **标准合规性**：
   - 所有建议必须符合民航规章（CCAR-121/91/135等）
   - 参考ICAO Annex 3气象服务标准
   - 遵循公司运行规范

3. **风险警示**：
   - 发现任何潜在风险必须明确警示
   - 风险等级评估保守
   - 紧急情况立即标注CRITICAL

4. **责任边界**：
   - 提供建议，不替代人工决策
   - 不越权给出运行指令
   - 关键决策建议人工复核

【分析质量要求 - 反模板化】
1. **基于数据分析**：你的分析必须基于当前METAR的具体数值（温度、风速、能见度等），不同数据必须产生不同分析
2. **禁止套话**：禁止输出"当前天气条件良好，各项指标均在正常范围内"等万能模板语句
3. **深度思考**：即使天气良好，也应分析温度露点差对积冰的潜在影响、风向变化趋势、气压系统含义等
4. **情境关联**：分析应结合机场地理位置（沿海/高原/内陆）、季节、时间（晨雾/午后对流）等因素
5. **差异化建议**：不同角色、不同天气组合必须给出截然不同的建议，不能千篇一律
"""


# ==================== 辅助函数 ====================

def get_system_prompt(role: str) -> str:
    """
    获取角色特定的系统提示词

    Args:
        role: 角色 (pilot/dispatcher/forecaster/ground_crew)

    Returns:
        系统提示词
    """
    base_prompt = SYSTEM_PROMPTS.get(role, SYSTEM_PROMPTS["dispatcher"])
    return f"{base_prompt}\n\n{SAFETY_CONSTRAINTS_TEMPLATE}"


def get_role_focus(role: str) -> str:
    """获取角色关注点"""
    return ROLE_FOCUS.get(role, "综合分析")


def get_role_name_cn(role: str) -> str:
    """获取角色中文名"""
    return ROLE_NAMES_CN.get(role, role)


